void sigUnblock(int32_t sig) {
   sigset_t mask;

   if (sig == 0)
      sigfillset(&mask);
   else {
      sigemptyset(&mask);
      sigaddset(&mask, (int)sig);
   }
   sigprocmask(SIG_UNBLOCK, &mask, NULL);
}

void sigChld(int n __attribute__((unused))) {
   int e, stat;
   pid_t pid;

   e = errno;
   while ((pid = waitpid(0, &stat, WNOHANG)) > 0)
      if (WIFSIGNALED(stat))
         fprintf(stderr, "%d SIG-%d\n", (int)pid, WTERMSIG(stat));
   errno = e;
}

