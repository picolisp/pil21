int32_t getLock(int32_t fd, off_t n, off_t len) {
   struct flock fl;

   fl.l_type = F_WRLCK;
   fl.l_whence = SEEK_SET;
   fl.l_start = n;
   fl.l_len = len;
   if (fcntl(fd, F_GETLK, &fl) < 0)
      return -1;
   return fl.l_type == F_UNLCK? 0 : fl.l_pid;
}

