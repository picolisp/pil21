static void tcSet(struct termios *p) {
   while (tcsetattr(STDIN_FILENO, TCSADRAIN, p)  &&  errno == EINTR);
}

void stopTerm(void) {
   sigset_t mask;

   if (Tio) {
      tcSet(&OrgTermio);
      sigUnblock(SIGTSTP);
      signal(SIGTSTP, SIG_DFL),  raise(SIGTSTP);
      if (Termio)
         tcSet(Termio);
   }
}

void setRaw(void) {
   if (Tio && !Termio) {
      *(Termio = malloc(sizeof(struct termios))) = OrgTermio;
      tcgetattr(STDIN_FILENO, Termio);
      Termio->c_iflag = 0;
      Termio->c_lflag = ISIG;
      Termio->c_cc[VMIN] = 1;
      Termio->c_cc[VTIME] = 0;
      tcSet(Termio);
   }
}

void setCooked(void) {
   if (Termio) {
      tcSet(&OrgTermio);
      free(Termio),  Termio = NULL;
   }
}

